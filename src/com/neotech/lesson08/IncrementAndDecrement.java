package com.neotech.lesson08;

public class IncrementAndDecrement {

	public static void main(String[] args) {

		int x = 10;
		// RE ASSIGN
		//x = 11;
		x = x + 1;
		
		//SHORTHAND OPERATION
		x +=1;
		
		//INCREMENT
		x++;
		System.out.println(x);
		int y = 50;
		y = y - 1 ;
		//SHORTHAND OPE
		y-= 1 ;
		//DECREMENT
		y -- ;
		
		System.out.println(y);

	}

}
