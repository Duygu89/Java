package com.neotech.lesson09;

public class ForLoopIntro {

	public static void main(String[] args) {
		
		
		//print numbers from 1 to 10
		
		//FOR LOOP 
		// FOR (init ; (semicolon required) condition; increment) {}
		
		
		
		
		int num = 1;
		
		while(num<=10)
		{
			System.out.println(num+" ");
			num++;
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
